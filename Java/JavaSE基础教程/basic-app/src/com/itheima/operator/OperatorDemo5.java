package com.itheima.operator;

public class OperatorDemo5 {
    public static void main(String[] args) {
        //关系运算符
        int a = 10, b = 10;
        double c = 10, d = 10.0;
        System.out.println(a == b);
        System.out.println(a == c);
        System.out.println(a == d);
        boolean rs3 = b != d;
        boolean rs4 = c == d;
        System.out.println(rs3);
        System.out.println(rs4);
    }
}
